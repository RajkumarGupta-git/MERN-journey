// Print a week using a number variable'day' with value 1 to 7

let day = 1;


switch(day) {
    case 1:
        console.log("Monday");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wednesday");
        break;
    case 4:
        console.log("Thursday");
        break;
    case 5:
        console.log("Friday");
        break;
    case 6:
        console.log("Saturday");
        break;
    case 7:
        console.log("Sunday is Fun Day.");
        break;
    default:
        console.log("Wrong Day");
        break;
}